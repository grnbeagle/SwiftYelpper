import Foundation

extension UIColor {
    class func yelpperRedColor() -> UIColor {
        return UIColor(red: 196/255, green: 18/255, blue: 0/255, alpha: 1)
    }

    class func yelpperLightGrayColor() -> UIColor {
        return UIColor(red: 222/255, green: 223/255, blue: 225/255, alpha: 1)
    }

}